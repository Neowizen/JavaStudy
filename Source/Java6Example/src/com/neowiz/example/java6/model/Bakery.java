package com.neowiz.example.java6.model;


public class Bakery {
	private String name;
	private Integer saleCnt;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public Integer getSaleCnt() {
		return saleCnt;
	}
	
	public void setSaleCnt(Integer saleCnt) {
		this.saleCnt = saleCnt;
	}
}
